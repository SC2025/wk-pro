var slideWK3Index = 0;
            
            function showSlidesWK3() {
              let i;
              let slidesWK3 = document.getElementsByClassName("mySlidesWK3");
              let dotsWK3 = document.getElementsByClassName("dotWK3");
              for (i = 0; i < slidesWK3.length; i++) {
                slidesWK3[i].style.display = "none";  
              }
              slideWK3Index++;
              if (slideWK3Index > slidesWK3.length) {slideWK3Index = 1}    
              for (i = 0; i < dotsWK3.length; i++) {
                dotsWK3[i].className = dotsWK3[i].className.replace(" active", "");
              }
              slidesWK3[slideWK3Index-1].style.display = "block";  
              dotsWK3[slideWK3Index-1].className += " active";
              setTimeout(showSlidesWK3, 2000); // Change image every 2 seconds
            };
            showSlidesWK3();
            
            
            
            
            
            
            
            

            
